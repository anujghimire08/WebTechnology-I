let arr = [49, -1, 6, 9, 2];

// add at last of arr
arr.push(6);
console.log(arr);

// delete at last of arr
arr.pop();
console.log(arr);

// add at first of arr
arr.unshift(0);
console.log(arr);

// delete at first of arr
arr.shift();
console.log(arr);

// reverse arr
arr.reverse();
console.log(arr);

// array sort
arr.sort((a,b) => a-b);
console.log(arr);




